Vue.component('toggle-tab', {
    template: `
        <div class="tab-group tab-group--small">
            <label v-for="(item, index) in tabs" :key="index" tabindex="0">
                <input 
                    type="radio"
                    :value="item"
                    v-model="radioModel"
                    @input='$emit("update", $event.target.value)'
                    name="check-tab"
                    tabindex="-1">
                <span>{{ item }}</span>
            </label>
        </div>
    `,
    model: {
        prop: "value",
        event: "update"
    },

    props: {
        tabs: {
            default: []
        },
        value: { required: true }
    },

    data() {
        return {
            radioModel: null
        }
    },

    watch: {
        'value'(val) {
            this.radioModel = val;
        }
    },

    created() {
        this.$nextTick(() => {
            this.radioModel = this.value;
        })
    }
});


